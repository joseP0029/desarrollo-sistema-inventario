    private void btnAnadirProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirProductoActionPerformed
        if(ckPerecible.isSelected()) {
        ProductoPerecible p = new ProductoPerecible(ckRefrigerado.isSelected(),Integer.parseInt(txtId.getText()), Integer.parseInt(txtCantidad.getText()), txtNombre.getText(), txtCategoria.getText(), txtFabricante.getText(), txtDetallesEnvio.getText(), Float.parseFloat(txtPrecio.getText()), agenda.proveedorEnPosicion(agenda.buscarProveedorPorNombre((String)cmbProveedor.getSelectedItem())));
        almacen.agregarProducto(p);
        } else {
            ProductoNoPerecible p = new ProductoNoPerecible(Integer.parseInt(txtId.getText()), Integer.parseInt(txtCantidad.getText()), txtNombre.getText(), txtCategoria.getText(), txtFabricante.getText(), txtDetallesEnvio.getText(), Float.parseFloat(txtPrecio.getText()), agenda.proveedorEnPosicion(agenda.buscarProveedorPorNombre((String)cmbProveedor.getSelectedItem())));
            almacen.agregarProducto(p);
        }
        this.dispose();
    }//GEN-LAST:event_btnAnadirProductoActionPerformed

